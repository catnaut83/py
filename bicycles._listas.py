# Lista em Python
bicycles = ['trek', 'cannondale', 'redline', 'specialized']

print(bicycles)

#Exibindo apenas um item da lista
print(bicycles[0])

print(bicycles[3])

#Formatando a saida de um item da lista

print(bicycles[1].title())

# O índice de uma lista começa com 0, e o último é -1.

print("Primeiro item da lista: " + bicycles[0])

print("Ultimo item da lista: " + bicycles[-1])

print("Minha proxima bicicleta sera uma " + bicycles[1].title())